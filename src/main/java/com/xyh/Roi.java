package com.xyh;

public class Roi {
    private int w;
    private int h;
    private int x;
    private int y;
    public void setW(int w) {
        this.w = w;
    }
    public int getW() {
        return w;
    }

    public void setH(int h) {
        this.h = h;
    }
    public int getH() {
        return h;
    }

    public void setX(int x) {
        this.x = x;
    }
    public int getX() {
        return x;
    }

    public void setY(int y) {
        this.y = y;
    }
    public int getY() {
        return y;
    }

}
